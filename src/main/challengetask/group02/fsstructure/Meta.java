package challengetask.group02.fsstructure;

public class Meta {
	
	private long atime;
	private long ctime;
	private long size;
	
	public Meta() {
		
	}
	
	public long getAtime() {
		return atime;
	}
	public void setAtime(long atime) {
		this.atime = atime;
	}
	public long getCtime() {
		return ctime;
	}
	public void setCtime(long ctime) {
		this.ctime = ctime;
	}
	public long getSize() {
		return size;
	}
	public void setSize(long size) {
		this.size = size;
	}
}